package com.struts2.bean;

public class Book {
  
  private String name;
  private String price;
  private int num;
  
  public String getName() {
	return name;
  }
  
  public void setName(String name) {
	this.name = name;
  }
  
  public String getPrice() {
	return price;
  }
  
  public void setPrice(String price) {
	this.price = price;
  }
  
  public int getNum() {
	return num;
  }
  
  public void setNum(int num) {
	this.num = num;
  }
  
  
}
